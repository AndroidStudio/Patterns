package patterns.module.builder;

class Shape {

    private int color;
    private int width;
    private int height;

    void setColor(int color) {
        this.color = color;
    }

    int getColor() {
        return color;
    }

    int getWidth() {
        return width;
    }

    void setWidth(int width) {
        this.width = width;
    }

    int getHeight() {
        return height;
    }

    void setHeight(int height) {
        this.height = height;
    }
}
